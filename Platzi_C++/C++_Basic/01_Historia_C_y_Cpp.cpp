// 🎯 EJEMPLO SIMBÓLICO: Evolución de "Hola Mundo"

#include <iostream>  // 📚 Esta línea conecta con décadas de historia

using namespace std;  // 🏠 Característica que C++ agregó sobre C

int main()  // 🚀 La función que Dennis Ritchie ayudó a crear
{
    // 👋 Un saludo que conecta el pasado con el presente
    cout << "🎉 ¡Hola desde la historia de la programación!" << endl;
    cout << "📜 Estás usando tecnología creada en los 70s y 80s" << endl;
    cout << "🚀 Que sigue siendo relevante en " << 2024 << endl;
    cout << endl;
    
    // 🕰️ Cronología rápida
    cout << "📅 CRONOLOGÍA:" << endl;
    cout << "   1970s: Dennis Ritchie crea C" << endl;
    cout << "   1980s: Bjarne Stroustrup crea C++" << endl;
    cout << "   2024:  ¡Tú estás aprendiendo C++!" << endl;
    cout << endl;
    
    cout << "🎯 ¡Ahora eres parte de esta increíble historia!" << endl;
    
    return 0;  // ✅ Retorno que ha funcionado por décadas
}

